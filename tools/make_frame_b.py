# Combine each column's 3 pages into a 24-bit word, shift up by 1 px, decompose
def shift_up(bytes_in, n):
    out = [0] * 96
    for col in range(32):
        word = bytes_in[col] | (bytes_in[32 + col] << 8) | (bytes_in[64 + col] << 16)
        word = (word >> n) & 0xFFFFFF
        out[col]      = word & 0xFF
        out[32 + col] = (word >> 8) & 0xFF
        out[64 + col] = (word >> 16) & 0xFF
    return out

