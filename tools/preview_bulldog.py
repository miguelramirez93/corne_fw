# Decode to native 128W x 32H (4 pages of 128 cols, each byte = 8 vertical pixels LSB=top)
native = Image.new('1', (128, 32), 1)
px = native.load()
for page in range(4):
    for x in range(128):
        byte = bulldog[page * 128 + x]
        for bit in range(8):
            if byte & (1 << bit):
                px[x, page * 8 + bit] = 0

# Save native (what bytes literally encode)
native.resize((640, 160), Image.NEAREST).save('/tmp/preview_native.png')

# Save 90 CW rotation (one possible OLED interpretation)
rot_cw = native.transpose(Image.ROTATE_270)  # ROTATE_270 in PIL = 90 CW
rot_cw.resize((160, 640), Image.NEAREST).save('/tmp/preview_portrait_cw.png')

# Save 90 CCW rotation (other possible OLED interpretation)
rot_ccw = native.transpose(Image.ROTATE_90)  # ROTATE_90 in PIL = 90 CCW
rot_ccw.resize((160, 640), Image.NEAREST).save('/tmp/preview_portrait_ccw.png')

